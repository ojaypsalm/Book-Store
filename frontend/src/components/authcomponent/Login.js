// TODO remove, this demo shouldn't need to reset the theme.

const defaultTheme = createTheme();

const Login=()=> {
const [email, setEmail]= useState('')
const[password, setPassword]= useState('')
const navigate = useNavigate()
const author1 = process.env.REACT_APP_AUTH_URL


  const handleSubmit = (event) => {
    event.preventDefault();

    

    //const rfrsh = new FormData(event.currentTarget);

    // const data = {
    //   email: rfrsh.email,
    //   password: rfrsh.password
    // }
    

    if(!email ||
       !password
      ){
      toast.error('fill up all details')
      return;
    }

    const data = {
      email:email,
      password: password
    };

    axios.post(`${ author1 }/login`, data)
    .then((response)=>{
      localStorage.setItem('token', response.data.token);
      toast.success('User logged in Successfully ')
      setTimeout(()=>{
        navigate('/home')
      }, 3000)
      
    })
    .catch((error)=>{
      console.log('Error:', error.message)
      toast.error('Error: '+ error.meaasge )
    })

  };

  return (
    <ThemeProvider theme={defaultTheme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
            <LockOutlinedIcon />
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign in
          </Typography>
          <Box component="form" onSubmit={handleSubmit} noValidate sx={{ mt: 1 }}>
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Email Address"
              name="email"
              autoComplete="email"
              autoFocus
              value={email}
              onChange={(e)=>setEmail(e.target.value)}
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"
              value={password}
              onChange={(e)=>setPassword(e.target.value)}
            />
            <FormControlLabel
              control={<Checkbox value="remember" color="primary" />}
              label="Remember me"
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign In
            </Button>
            <Grid container>
              <Grid item xs>
                <Link href="/" variant="body2">
                  Forgot password?
                </Link>
              </Grid>


              <Grid item>
                <Link href="register" variant="body2">
                  {"Don't have an account? Sign Up"}
                </Link>
              </Grid>
            </Grid>
          </Box>
        </Box>
        <Copyright sx={{ mt: 8, mb: 4 }} />
      </Container>
      <ToastContainer />
    </ThemeProvider>
  );
}

export default Login